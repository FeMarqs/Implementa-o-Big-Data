import random

def contar_somas_lancamentos(n):
    # Inicializa um dicion√°rio para contar a quantidade de cada soma poss√≠vel (de 2 a 12)
    contagem = {i: 0 for i in range(2, 13)}

    for _ in range(n):
        # Simula o lan√ßamento de dois dados D6
        dado1 = random.randint(1, 6)
        dado2 = random.randint(1, 6)

        # Soma os resultados dos dois dados
        soma = dado1 + dado2

        # Incrementa a contagem da soma obtida
        contagem[soma] += 1

    # Exibe a quantidade de vezes que cada soma ocorreu
    for soma in range(2, 13):
        print(f"Soma {soma}: {contagem[soma]} vezes")

# Solicita o n√∫mero de lan√ßamentos ao usu√°rio
n = int(input("Digite o n√∫mero de vezes que os dados ser√£o lan√ßados: "))

# Chama a fun√ß√£o para realizar os lan√ßamentos e contar as somas
contar_somas_lancamentos(n)